package comjava;

public class Ejercicio1 {

	public static void main(String[] args) {
		double var1 = 5;
		double var2 = 4.2;
		double var3 = 2;
		
		double suma = var1 + var2 + var3;
		double resta = var1 - var2 - var3;
		double multiplicacion = var1 * var2 * var3;
		double division = var1 / var2 / var3;
		
		
		
		System.out.println("Suma de Varibales:" + suma);
		System.out.println("Resta de Varibales:" + resta);
		System.out.println("Multiplicacion de Varibales:" + multiplicacion);
		System.out.println("Division de Varibales:" + division);
	}

}
